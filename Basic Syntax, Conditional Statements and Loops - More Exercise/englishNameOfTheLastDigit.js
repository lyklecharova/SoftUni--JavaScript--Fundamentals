function englishNameOfTheLastDigit(input) {
    let lastElement = String(input);
    let last = lastElement.length;

    if (lastElement[last - 1] == 0) {
        console.log("zero");
    }
    if (lastElement[last - 1] == 1) {
        console.log("one");
    }
    if (lastElement[last - 1] == 2) {
        console.log("two");
    }
    if (lastElement[last - 1] == 3) {
        console.log("three");
    }
    if (lastElement[last - 1] == 4) {
        console.log("four");
    }
    if (lastElement[last - 1] == 5) {
        console.log("five");
    }
    if (lastElement[last - 1] == 6) {
        console.log("six");
    }
    if (lastElement[last - 1] == 7) {
        console.log("seven");
    }
    if (lastElement[last - 1] == 8) {
        console.log("eight");
    }
    if (lastElement[last - 1] == 9) {
        console.log("nine");
    }
}
